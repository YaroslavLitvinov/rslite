pub unsafe extern "C" fn sqlite3AuthRead(
    mut pParse: *mut crate::src::headers::sqliteInt_h::Parse,
    mut pExpr: *mut crate::src::headers::sqliteInt_h::Expr,
    mut pSchema: *mut crate::src::headers::sqliteInt_h::Schema,
    mut pTabList: *mut crate::src::headers::sqliteInt_h::SrcList,
) {
    let mut pTab: *mut crate::src::headers::sqliteInt_h::Table =
        ::core::ptr::null_mut::<crate::src::headers::sqliteInt_h::Table>();
    let mut zCol: *const ::core::ffi::c_char = ::core::ptr::null::<::core::ffi::c_char>();
    let mut iSrc: ::core::ffi::c_int = 0;
    let mut iDb: ::core::ffi::c_int = 0;
    let mut iCol: ::core::ffi::c_int = 0;
    iDb = crate::src::src::prepare::sqlite3SchemaToIndex(
        (*pParse).db as *mut crate::src::headers::sqliteInt_h::sqlite3,
        pSchema as *mut crate::src::headers::sqliteInt_h::Schema,
    );
    if iDb < 0 as ::core::ffi::c_int {
        return;
    }
    if (*pExpr).op as ::core::ffi::c_int == crate::src::parse::TK_TRIGGER {
        pTab = (*pParse).pTriggerTab;
    } else {
        iSrc = 0 as ::core::ffi::c_int;
        while iSrc < (*pTabList).nSrc {
            if (*pExpr).iTable
                == (*(&raw mut (*pTabList).a as *mut crate::src::headers::sqliteInt_h::SrcItem)
                    .offset(iSrc as isize))
                .iCursor
            {
                pTab = (*(&raw mut (*pTabList).a
                    as *mut crate::src::headers::sqliteInt_h::SrcItem)
                    .offset(iSrc as isize))
                .pSTab;
                break;
            } else {
                iSrc += 1;
            }
        }
    }
    iCol = (*pExpr).iColumn as ::core::ffi::c_int;
    if pTab.is_null() {
        return;
    }
    if iCol >= 0 as ::core::ffi::c_int {
        zCol = (*(*pTab).aCol.offset(iCol as isize)).zCnName;
    } else if (*pTab).iPKey as ::core::ffi::c_int >= 0 as ::core::ffi::c_int {
        zCol = (*(*pTab).aCol.offset((*pTab).iPKey as isize)).zCnName;
    } else {
        zCol = b"ROWID\0" as *const u8 as *const ::core::ffi::c_char;
    }
    if crate::src::headers::sqlite3_h::SQLITE_IGNORE
        == sqlite3AuthReadCol(pParse, (*pTab).zName, zCol, iDb)
    {
        (*pExpr).op = crate::src::parse::TK_NULL as crate::src::ext::rtree::rtree::u8_0;
    }
}
